"""Formats and prepends SOCIAL_DRAFTS.md — ready-to-copy-paste social content,
not auto-published anywhere (see docs/deployment.md). One Opportunity per run,
picked by LLMSearchPort.pick_viral_opportunity from repository.list_viral_candidates.

The evidence link is appended here, deterministically, from the Opportunity's
own pain-point data — never generated by the LLM (see ports.SocialDraftCopy),
so it can never be hallucinated or mangled.
"""

from __future__ import annotations

from pain_point_pipeline.markdown_files import prepend_section
from pain_point_pipeline.models import Opportunity
from pain_point_pipeline.ports import SocialDraftCopy

_TITLE = "# Social Drafts\n\n"


def _evidence_link(opportunity: Opportunity) -> str:
    return opportunity.pain_points[0].raw_item.url if opportunity.pain_points else ""


def format_social_draft(date: str, opportunity: Opportunity, copy: SocialDraftCopy) -> str:
    link = _evidence_link(opportunity)
    x_closer = f"{copy.x_closer} {link}".strip()
    tweets = [copy.x_hook, *copy.x_body, x_closer]
    x_thread = "\n\n".join(f"{n}. {tweet}" for n, tweet in enumerate(tweets, start=1))
    linkedin_comment = f"Source: {link}" if link else "(no source link available)"

    return "\n".join(
        [
            f"## {date} — {opportunity.title}",
            "",
            f"**{opportunity.frequency} reports from {opportunity.distinct_authors} people**",
            "",
            "### X (thread)",
            "",
            x_thread,
            "",
            "### LinkedIn (post)",
            "",
            copy.linkedin_post,
            "",
            "### LinkedIn (first comment — post right after, keeps the link out of the main post)",
            "",
            linkedin_comment,
            "",
        ]
    )


def prepend_social_draft(path: str, section: str) -> None:
    prepend_section(path, _TITLE, section)
